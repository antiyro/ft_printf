#include "libft.h"

int				ft_nbrlen(int nb)
{
	int			i;
	long long	n;

	n = nb;
	i = 0;
	if (n == 0)
		return (1);
	if (n < 0)
	{
		i++;
		n *= -1;
	}
	while (n > 0)
	{
		n /= 10;
		i++;
	}
	return (i);
}
